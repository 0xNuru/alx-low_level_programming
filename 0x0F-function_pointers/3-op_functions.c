/**
* op_add - function name
* @a: first operand
* @b: second operand
*
* Description: a function that adds two ints
* Return: sum of two ints
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - function name
 * @a: first operand
 * @b: second operand
 *
 * Description: a function that subtracts two ints
 * Return: difference between two ints
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - function name
 * @a: first operand
 * @b: second operand
 *
 * Description: a function that multiplies two ints
 * Return: product of two ints
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function name
 * @a: first operand
 * @b: second operand
 *
 * Description: a function that div two ints
 * Return: quotient of two ints
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - function name
 * @a: first operand
 * @b: second operand
 *
 * Description: a function that finds the mod of two ints
 * Return: remainder of two ints
 */

int op_mod(int a, int b)
{
	return (a % b);
}
